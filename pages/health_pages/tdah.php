    <h2 class="pb-4 card-title text-center">Transtorno do Déficit de Atenção/Hiperatividade (TDAH)</h2>
    <img src="../../resources/tdah.png" class="w-25 center-img d-none d-lg-block" alt="">

    <div class="row d-flex justify-content-center">
        <div class="col-8">
            <h4 class="pb-2" id="oque">O que é</h4>
            <p class="lead subtopic-content"> O Transtorno do Déficit de Atenção com Hiperatividade (TDAH) é um
                transtorno neurobiológico, de causas genéticas, que aparece na infância e frequentemente acompanha o
                indivíduo por toda a sua vida. </p>
            <p class="lead subtopic-content">De acordo com o DSM.IV, o manual de classificação das doenças mentais, a
                síndrome pode ser classificada em três tipos:</p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• TDAH com predomínio de sintomas de desatenção</li>
                <li class="list-group-item border-0">• TDAH com predomínio de sintomas de hiperatividade/impulsividade
                </li>
                <li class="list-group-item border-0">• TDAH combinado</li>
            </ul>
            <p class="lead subtopic-content">Em todas as faixas etárias, portadores do transtorno estão sujeitos a
                desenvolver comorbidades, isto é, a desenvolver simultaneamente distúrbios psiquiátricos, como ansiedade
                e depressão. Na adolescência, o risco maior está no uso abusivo do álcool e de outras drogas.</p>

            <h4 class="pb-2" id="oque">Sintomas</h4>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• Quando predominado a Desatenção os portadores do transtorno
                    apresentam dificuldade maior de concentração, de organizar atividades, de seguir instruções, e podem
                    saltar de uma tarefa inacabada para outra, sem nunca terminar aquilo que começaram. Além disso, são
                    pessoas que se distraem facilmente e frequentemente esquecem o que tinham para fazer ou onde
                    colocaram seus pertences. Não conseguem também prestar atenção em detalhes, demoram para iniciar as
                    tarefas e cometem erros por absoluto descuido e distração. </li>
                <li class="list-group-item border-0">• Quando predominado a Hiperatividade-Impulsividade os pacientes
                    são inquietos, agitados e falam muito. Dificilmente conseguem participar de atividades sedentárias e
                    manter silêncio durante as brincadeiras ou realização dos trabalhos. Se é a impulsividade que se
                    destaca os sinais mais marcantes são a impaciência, o agir sem pensar, a dificuldade para ouvir as
                    perguntas até o fim, a precipitação para falar e a intromissão nos assuntos, conversas e atividades
                    alheias.</li>
            </ul>

            <h4 class="pb-2" id="oque">Tratamento</h4>
            <p class="lead"> O tratamento do TDAH pode ser realizado através de: </p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• Medicamentos</li>
                <li class="list-group-item border-0">• Psicoterapia</li>
            </ul>
