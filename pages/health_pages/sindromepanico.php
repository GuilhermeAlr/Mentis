            <h4 class="pb-2" id="oque">1. O que é</h4>
            <p class="lead subtopic-content">Síndrome do Pânico é uma condição diretamente associada a crises de
                ansiedade aguda que ocorrem de forma repentina, trazendo medo e desespero ao indivíduo. </p>
            <p class="lead subtopic-content pb-3">Tais crises tendem a durar no máximo 10 minutos, e trazem a pessoa uma
                sensação de estarem morrendo ou enlouquecendo, e que estão sem controle sobre si mesmas.</p>


            <h4 class="pb-2" id="oque">2. Sintomas</h4>
            <p class="lead">Entre seus sintomas se encontram:</p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">- Despersonalização e desrealização (respectivamente a impressão de se estar vivendo num sonho, afastado da realidade, e a incapacidade de diferenciar a realidade da fantasia)</li>
                <li class="list-group-item border-0">- Sensação de falta de ar e sufocamento</li>
                <li class="list-group-item border-0">- Tontura</li>
                <li class="list-group-item border-0">- Ondas de calor e calafrios</li>
                <li class="list-group-item border-0">- Depressão</li>
                <li class="list-group-item border-0">- Ansiedade</li>
                <li class="list-group-item border-0">- Dor no peito; palpitação e taquicardia (distúrbios que provocam a sensação do ritmo cardíaco falhando)</li>
            </ul>

            <h4 class="pb-2" id="oque">3. Tratamento</h4>
            <p class="lead">Pode ser tratado com:</p>
            <ul class="list-group pb-5">
                <li class="list-group-item border-0">- Medicação através do uso de antidepressivos
                <li class="list-group-item border-0">- Tratamento psicoterapêutico (especialmente a terapia cognitivo-comportamental).
                </li>
            </ul>

