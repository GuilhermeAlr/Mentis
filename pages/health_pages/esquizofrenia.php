            <h4 class="pb-2" id="oque">O que é</h4>
            <p class="lead subtopic-content"> A esquizofrenia é uma doença mental crônica e incapacitante, que
                geralmente se manifesta na adolescência ou início da idade adulta, entre 20 e 30 anos de idade. Sua
                frequência na população em geral é da ordem de 1 para cada 100 pessoas. No Brasil, estima-se que há
                cerca de 1,6 milhão de esquizofrênicos. Até hoje, não foi descoberta a causa da esquizofrenia, mas
                alguns fatores podem desencadear a doença como por exemplo: </p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• <span class="fw-bold">Fatores hereditários</span> - parentes de
                    primeiro grau de um
                    esquizofrênico têm mais chances de desenvolver a doença do que as pessoas em geral. É o fator de
                    risco mais significativo. </li>
                <li class="list-group-item border-0">• <span class="fw-bold">Fatores ambientais</span> - complicações da
                    gravidez e do parto,
                    infecções e outras doenças que possam ter alterado o desenvolvimento do sistema nervoso no período
                    de gestação. </li>
                <li class="list-group-item border-0">• <span class="fw-bold">Alterações neuroquímicas</span> - problemas
                    com certas substâncias
                    químicas do cérebro, incluindo neurotransmissores chamados dopamina e glutamato, podem contribuir
                    para a esquizofrenia. Assim como o uso de drogas psicoativas, que alteram a mente.</li>
            </ul>

            <h4 class="pb-2" id="oque">Sintomas</h4>
            <p class="lead subtopic-content pb-3"> Como a doença se manifesta de forma variada os sintomas são divididos
                entre os sintomas positivos e os negativos.</p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0"><span class="fw-bold">Principais sintomas positivos:</span></li>
                <li class="list-group-item border-0">• Delírios - são ideias ou pensamentos que não correspondem à
                    realidade, mas que a pessoa tem convicção absoluta. Por exemplo, ela acredita que está sendo
                    vigiada, ou perseguida, ou observada por câmeras escondidas; acredita que os vizinhos ou as pessoas
                    que passam na rua querem lhe fazer mal. </li>
                <li class="list-group-item border-0">• Alucinações - são percepções irreais dos órgãos dos sentidos. As
                    alucinações auditivas são as mais frequentes. A pessoa pode ouvir vozes que dão ordens de como agir
                    ou que falam sobre ela. Mais raramente, podem ocorrer outras formas de alucinações, como visuais,
                    táteis ou olfativas.</li>
                <li class="list-group-item border-0">• Alterações do pensamento - as ideias podem se tornar confusas,
                    desorganizadas ou desconexas, tornando o discurso da pessoa difícil de entender.</li>
                <li class="list-group-item border-0"><span class="fw-bold">Principal sintoma negativo:</span></li>
                <li class="list-group-item border-0">• Alterações da afetividade - a pessoa perde a capacidade de
                    expressar suas emoções e de reagir emocionalmente às situações, ficando indiferente e sem expressão
                    afetiva. Outras vezes, apresenta reações afetivas que são inadequadas em relação ao contexto em que
                    se encontra.</li>
                <li class="list-group-item border-0"><span class="fw-bold">Alguns de outros sintomas que podem ser
                        observados:</span></li>
                <li class="list-group-item border-0">• Diminuição da motivação</li>
                <li class="list-group-item border-0">• Dificuldade de concentração</li>
                <li class="list-group-item border-0">• Alterações da motricidade</li>
                <li class="list-group-item border-0">• Desconfiança excessiva</li>
                <li class="list-group-item border-0">• Indiferença</li>

            </ul>


            <h4 class="pb-2" id="oque">Tratamento</h4>
