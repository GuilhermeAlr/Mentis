                <li class="list-group-item border-0">• Desorientação no tempo e no espaço</li>
                <li class="list-group-item border-0">• Comprometimento do julgamento</li>
                <li class="list-group-item border-0">• Comprometimento do raciocínio abstrato</li>
                <li class="list-group-item border-0">• Perda frequente de objetos</li>
                <li class="list-group-item border-0">• Alteração do humor e do comportamento</li>
                <li class="list-group-item border-0">• Mudança da personalidade</li>
                <li class="list-group-item border-0">• Perda da iniciativa</li>
            </ul>

