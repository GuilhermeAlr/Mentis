            <h4 class="pb-2" id="oque">O que é</h4>
            <p class="lead subtopic-content"> TOC, ou transtorno obsessivo-compulsivo, é um distúrbio psiquiátrico de
                ansiedade descrito no “Manual de Diagnóstico e Estatística de Transtornos Mentais – DSM V” da Associação
                de Psiquiatria Americana. A principal característica do TOC é a presença de crises recorrentes de
                obsessões e compulsões. Estas obsessões podem ser pensamentos, ideias e imagens que invadem a pessoa
                insistentemente, sem que ela queira. </p>
            <p class="lead subtopic-content"> Essa vontade compulsiva de realizar estes rituais fica martelando dentro
                da cabeça da pessoa. Dessa forma o único jeito de se livrar desse sentimento por algum tempo é realizar
                o ritual próprio da compulsão, seguindo regras e etapas rígidas e pré-estabelecidas, que ajudam a
                aliviar a ansiedade. </p>

            <h4 class="pb-2" id="oque">Sintomas</h4>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• O principal sintoma da doença é a presença de pensamentos
                    obsessivos que levam à realização de um ritual compulsivo para aplacar a ansiedade que toma conta da
                    pessoa.</li>
                <li class="list-group-item border-0">• Dificuldade para pronunciar certas palavras</li>
                <li class="list-group-item border-0">• Indecisão diante de situações corriqueiras por medo que uma
                    escolha errada possa desencadear alguma desgraça</li>
                <li class="list-group-item border-0">• Pensamentos agressivos relacionados com morte, acidentes ou
                    doenças</li>
            </ul>

            <h4 class="pb-2" id="oque">Tratamento</h4>
            <p class="lead"> O tratamento do TOC pode ser realizado através de: </p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• Medicamentos</li>
                <li class="list-group-item border-0">• Terapia comportamental</li>
            </ul>
