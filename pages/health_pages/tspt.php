    <div class="row d-flex justify-content-center">
        <div class="col-8">
            <h4 class="pb-2" id="oque">O que é</h4>
            <p class="lead subtopic-content"> O transtorno do estresse pós-traumático (TEPT) é um distúrbio da ansiedade
                caracterizado por um conjunto de sinais e sintomas físicos, psíquicos e emocionais em decorrência de o
                portador ter sido vítima ou testemunha de atos violentos ou de situações traumáticas que, em geral,
                representaram ameaça à sua vida ou à vida de terceiros. </p>
            <p class="lead subtopic-content"> Quando se recorda do fato, ele revive o episódio, como se estivesse
                ocorrendo naquele momento e com a mesma sensação de dor e sofrimento que o agente estressor provocou.
                Essa recordação, conhecida como revivescência, desencadeia alterações neurofisiológicas e mentais.</p>

            <h4 class="pb-2" id="oque">Sintomas</h4>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• <span class="fw-bold">Lembranças persistentes</span>: reviver
                    involuntariamente o trauma através de memórias angustiantes e repetitivas, pesadelos ou sensação de
                    que o evento traumático está acontecendo novamente (“flashbacks”). </li>
                <li class="list-group-item border-0">• <span class="fw-bold">Reações físicas</span>: circunstâncias que
                    remetem à lembrança do trauma podem desencadear sintomas fisiológicos, incluindo sudorese, náusea e
                    tremores. </li>
                <li class="list-group-item border-0">• <span class="fw-bold">Comportamento de esquiva</span>:evitar
                    lugares, pessoas e atividades que trazem recordações dolorosas. A pessoa também pode ser incapaz de
                    lembrar ou falar sobre o ocorrido.</li>
                <li class="list-group-item border-0">• <span class="fw-bold">Excitação exagerada</span>: inclui
                    ficar em estado de alerta constante, explosões de raiva, dificuldade para dormir e para se
                    concentrar. </li>
                <li class="list-group-item border-0">• <span class="fw-bold">Crenças e emoções negativas</span>: por
                    exemplo, dificuldade de confiar nos outros e manter relacionamentos próximos; perda de interesse em
                    atividades; sentimento de culpa e vergonha. </li>
            </ul>

            <h4 class="pb-2" id="oque">Tratamento</h4>
            <p class="lead"> O tratamento do transtorno de estresse pós-traumático pode envolver: </p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• Medicamentos</li>
                <li class="list-group-item border-0">• Psicoterapia</li>
            </ul>
