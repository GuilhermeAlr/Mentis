                expectativa apreensiva”, persistente e de difícil controle. É importante ressaltar também que, nesses
                casos, o nível de ansiedade é desproporcional aos acontecimentos geradores do transtorno, causa muito
                sofrimento e interfere na qualidade de vida e no desempenho familiar, social e profissional dos
                pacientes. </p>

